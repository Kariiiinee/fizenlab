// Translation manager
class TranslationManager {
    constructor() {
        this.currentLang = this.getSavedLanguage() || 'fr';
        this.translations = translations;
    }

    getSavedLanguage() {
        return localStorage.getItem('fizenlab_lang') || 'fr';
    }

    saveLanguage(lang) {
        localStorage.setItem('fizenlab_lang', lang);
    }

    setLanguage(lang) {
        if (!this.translations[lang]) {
            console.error(`Language ${lang} not found`);
            return;
        }

        this.currentLang = lang;
        this.saveLanguage(lang);
        this.updatePage();
        this.updateLanguageIndicator();
    }

    translate(key) {
        return this.translations[this.currentLang][key] || key;
    }

    updatePage() {
        // Update all elements with data-i18n attribute
        document.querySelectorAll('[data-i18n]').forEach(element => {
            const key = element.getAttribute('data-i18n');
            const translation = this.translate(key);

            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                if (element.hasAttribute('placeholder')) {
                    element.placeholder = translation;
                } else {
                    element.value = translation;
                }
            } else {
                element.textContent = translation;
            }
        });

        // Update elements with data-i18n-html (for HTML content)
        document.querySelectorAll('[data-i18n-html]').forEach(element => {
            const key = element.getAttribute('data-i18n-html');
            const translation = this.translate(key);
            element.innerHTML = translation;
        });
    }

    updateLanguageIndicator() {
        const currentLangSpan = document.getElementById('currentLang');
        if (currentLangSpan) {
            currentLangSpan.innerHTML = this.currentLang === 'en' ? 'üá∫üá∏' : 'üá´üá∑';
        }
    }

    init() {
        // Set initial language
        this.updatePage();
        this.updateLanguageIndicator();

        // Add event listeners to language options
        document.querySelectorAll('.lang-option').forEach(option => {
            option.addEventListener('click', (e) => {
                const lang = e.target.getAttribute('data-lang') ||
                    (e.target.textContent.includes('üá∫üá∏') ? 'en' : 'fr');
                this.setLanguage(lang);
            });
        });
    }
}

// Initialize translation manager when DOM is ready
document.addEventListener('DOMContentLoaded', () => {
    window.translationManager = new TranslationManager();
    window.translationManager.init();
});

// Global function for inline onclick handlers
function setLanguage(lang) {
    if (window.translationManager) {
        window.translationManager.setLanguage(lang);
    }
}
